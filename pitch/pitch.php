// Porcentaje radial
function animateCircles() {
  document.querySelectorAll('.criterio').forEach(criterio => {
    const circle = criterio.querySelector('.circle-progress');
    const text = criterio.querySelector('.circle-text');
    const target = +criterio.getAttribute('data-porcentaje');
    let progress = 0;
    let interval = setInterval(() => {
      progress++;
      let dash = (progress * 100) / 100;
      circle.setAttribute('stroke-dasharray', `${dash}, 100`);
      text.textContent = progress + '%';
      if(progress >= target) clearInterval(interval);
    }, 20);
  });
}

const observerCriterios = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if(entry.isIntersecting){
      animateCircles();
      observerCriterios.unobserve(entry.target);
    }
  });
}, { threshold: 0.5 });
observerCriterios.observe(document.querySelector('.evaluacion'));
