const items = document.querySelectorAll('.timeline-item');
let current = 0;

function advanceTimeline() {
  // Apagar todos
  items.forEach(item => {
    item.classList.remove('active');
    item.querySelector('.icon').classList.remove('pulse');
  });

  // Activar solo el actual
  const item = items[current];
  item.classList.add('active');
  item.querySelector('.icon').classList.add('pulse');

  // Pasar al siguiente
  current = (current + 1) % items.length;
  setTimeout(advanceTimeline, 2000); // duración de la animación por evento
}

